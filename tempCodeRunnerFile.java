public class Switch {
    public static void main(String[] args) {
        int hello = 10;
        switch(hello){
            case 0:
                System.out.println("Zero");
            case 1:
                System.out.println("one");
            case 2:
                System.out.println("two");
            case 10:
                System.out.println("ten");
            break;
            case 5:
                System.out.println("five");
        }
    }
}
